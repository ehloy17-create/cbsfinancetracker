-- -----------------------------------------------
-- Function: generate_po_number
-- Generates PO-YYYYMMDD-NNNN format
-- -----------------------------------------------
CREATE OR REPLACE FUNCTION generate_po_number()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
  today_str text;
  seq_num   int;
  new_po_number text;
BEGIN
  today_str := to_char(CURRENT_DATE, 'YYYYMMDD');

  SELECT COUNT(*) + 1
    INTO seq_num
    FROM purchase_orders
   WHERE po_number LIKE 'PO-' || today_str || '-%';

  new_po_number := 'PO-' || today_str || '-' || LPAD(seq_num::text, 4, '0');
  NEW.po_number := new_po_number;
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS trg_generate_po_number ON purchase_orders;
CREATE TRIGGER trg_generate_po_number
  BEFORE INSERT ON purchase_orders
  FOR EACH ROW
  WHEN (NEW.po_number = '')
  EXECUTE FUNCTION generate_po_number();

-- -----------------------------------------------
-- Function: sync_po_totals
-- Recalculates subtotal and total_amount on the PO
-- whenever items are inserted/updated/deleted
-- Also auto-updates PO status based on received qty
-- -----------------------------------------------
CREATE OR REPLACE FUNCTION sync_po_totals()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
  v_po_id uuid;
  v_subtotal numeric(14,2);
  v_total numeric(14,2);
  v_total_ordered numeric(12,3);
  v_total_received numeric(12,3);
  v_new_status po_status;
  v_current_status po_status;
BEGIN
  -- Determine which PO to update
  IF TG_OP = 'DELETE' THEN
    v_po_id := OLD.po_id;
  ELSE
    v_po_id := NEW.po_id;
  END IF;

  -- Sum line totals
  SELECT
    COALESCE(SUM(qty_ordered * unit_cost), 0),
    COALESCE(SUM(qty_ordered), 0),
    COALESCE(SUM(qty_received), 0)
  INTO v_subtotal, v_total_ordered, v_total_received
  FROM purchase_order_items
  WHERE po_id = v_po_id;

  -- Get current header values for charges
  SELECT
    status,
    v_subtotal + tax_amount + other_charges - discount_amount
  INTO v_current_status, v_total
  FROM purchase_orders
  WHERE id = v_po_id;

  -- Determine new status (only auto-advance for approved+)
  IF v_current_status IN ('approved', 'partially_received', 'fully_received') THEN
    IF v_total_received = 0 THEN
      v_new_status := 'approved';
    ELSIF v_total_received >= v_total_ordered THEN
      v_new_status := 'fully_received';
    ELSE
      v_new_status := 'partially_received';
    END IF;
  ELSE
    v_new_status := v_current_status;
  END IF;

  UPDATE purchase_orders
     SET subtotal     = v_subtotal,
         total_amount = v_subtotal + tax_amount + other_charges - discount_amount,
         status       = v_new_status,
         updated_at   = now()
   WHERE id = v_po_id;

  RETURN COALESCE(NEW, OLD);
END;
$$;

DROP TRIGGER IF EXISTS trg_sync_po_totals ON purchase_order_items;
CREATE TRIGGER trg_sync_po_totals
  AFTER INSERT OR UPDATE OR DELETE ON purchase_order_items
  FOR EACH ROW
  EXECUTE FUNCTION sync_po_totals();
