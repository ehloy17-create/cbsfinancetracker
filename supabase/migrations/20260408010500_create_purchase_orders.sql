/*
  # Purchase Orders Module

  ## Summary
  Adds a full purchase order system to the inventory module.
  Supports multi-line POs per supplier and location, with automatic
  total computation and received quantity tracking.

  ## New Tables

  ### purchase_orders
  - Header record per PO: supplier, location, dates, status, totals
  - po_number is auto-generated (PO-YYYYMMDD-NNNN sequence per day)
  - Statuses: draft, approved, partially_received, fully_received, closed, cancelled

  ### purchase_order_items
  - One row per product line on a PO
  - Tracks ordered qty, received qty, unit cost, and line total
  - received_qty is updated when goods are received (future receiving module)

  ## Computed Columns (via trigger)
  - purchase_orders.subtotal = sum of (qty_ordered * unit_cost) per line
  - purchase_orders.total_amount = subtotal + tax_amount + other_charges - discount_amount
  - Status auto-advances to partially_received / fully_received when received qty changes

  ## Security
  - RLS enabled on both tables
  - Authenticated users can read
  - Admins can insert / update / delete

  ## Notes
  - po_number is unique and generated by a before-insert trigger
  - Cancelling a PO only allowed from draft or approved status
  - Closing is a manual admin action after fully received
*/

-- -----------------------------------------------
-- PO status enum
-- -----------------------------------------------
DO $$
BEGIN
  IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'po_status') THEN
    CREATE TYPE po_status AS ENUM (
      'draft',
      'approved',
      'partially_received',
      'fully_received',
      'closed',
      'cancelled'
    );
  END IF;
END $$;

-- -----------------------------------------------
-- purchase_orders
-- -----------------------------------------------
CREATE TABLE IF NOT EXISTS purchase_orders (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  po_number text UNIQUE NOT NULL DEFAULT '',
  supplier_id uuid NOT NULL REFERENCES inv_suppliers(id) ON DELETE RESTRICT,
  location_id uuid NOT NULL REFERENCES inv_locations(id) ON DELETE RESTRICT,
  status po_status NOT NULL DEFAULT 'draft',
  order_date date NOT NULL DEFAULT CURRENT_DATE,
  expected_date date,
  approved_date date,
  approved_by uuid REFERENCES profiles(id) ON DELETE SET NULL,
  notes text NOT NULL DEFAULT '',
  terms text NOT NULL DEFAULT '',
  subtotal numeric(14,2) NOT NULL DEFAULT 0,
  tax_amount numeric(14,2) NOT NULL DEFAULT 0,
  discount_amount numeric(14,2) NOT NULL DEFAULT 0,
  other_charges numeric(14,2) NOT NULL DEFAULT 0,
  total_amount numeric(14,2) NOT NULL DEFAULT 0,
  created_by uuid REFERENCES profiles(id) ON DELETE SET NULL,
  updated_by uuid REFERENCES profiles(id) ON DELETE SET NULL,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_po_supplier ON purchase_orders(supplier_id);
CREATE INDEX IF NOT EXISTS idx_po_location ON purchase_orders(location_id);
CREATE INDEX IF NOT EXISTS idx_po_status ON purchase_orders(status);
CREATE INDEX IF NOT EXISTS idx_po_order_date ON purchase_orders(order_date DESC);
CREATE INDEX IF NOT EXISTS idx_po_number ON purchase_orders(po_number);

ALTER TABLE purchase_orders ENABLE ROW LEVEL SECURITY;

